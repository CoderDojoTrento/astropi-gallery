def get_char_pixels(char):
    """
    Returns an 8-row x 5-col grid of booleans for the given character.
    Each row is a list of 5 bools (True = pixel on).
    """
    cols = _FONT_COLS.get(char, _FONT_COLS.get('?'))
    rows = []
    for row in range(7):  # 7 data rows
        row_pixels = []
        for col in range(5):
            row_pixels.append(bool(cols[col] & (1 << row)))
        rows.append(row_pixels)
    # 8th row is blank (baseline padding)
    rows.append([False] * 5)
    return rows
